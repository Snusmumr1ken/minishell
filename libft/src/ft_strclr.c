#include "../includes/libft.h"

void	ft_strclr(char *s)
{
	int size;
	int i;

	if (!s)
		return ;
	size = ft_strlen(s);
	i = 0;
	while (i < size)
	{
		s[i] = '\0';
		i++;
	}
}
